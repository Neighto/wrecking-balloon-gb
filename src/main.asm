SECTION "rom", ROM0[$100]
	jp EntryPoint
	ds $150 - @, 0 ; Make room for the header

EntryPoint:
	; Shut down audio circuitry
	ld a, 0
	ld [rNR52], a

	; Do not turn the LCD off outside of VBlank
	call Wait_VBlank

	; Turn the LCD off
	call LCD_Off

; 	; Copy the tile data
	ld bc, Tiles
	ld hl, $9000
	ld de, TilesEnd - Tiles
	call memcpy

	; Copy the tilemap
	ld bc, Tilemap
	ld hl, $9800
	ld de, TilemapEnd - Tilemap
	call memcpy

	; Turn the LCD on
	call LCD_On

	; During the first (blank) frame, initialize display registers
	ld a, %11100100
	ld [rBGP], a

Done:
	jp Done


